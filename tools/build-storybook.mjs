#!/usr/bin/env node
/* Генератор storybook-страницы (GitHub Pages) для arrow-components.
   Сканирует awds-component-* / component.meta.json и собирает index.html:
   сайдбар со списком компонентов + iframe с их preview.html.

   Перезапускать после публикации нового/обновлённого компонента:
     node tools/build-storybook.mjs

   Превью (preview.html) — author/ACB-owned, НЕ трогаются. Этот скрипт
   генерирует только index.html в корне репозитория. */

import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

// Порядок в сайдбаре (как в README); не перечисленные — в конец по алфавиту.
const ORDER = [
  'button', 'button-overhung', 'link', 'avatar', 'notice', 'range', 'table',
];

function titleize(name) {
  return name.split('-').map(w => w[0].toUpperCase() + w.slice(1)).join(' ');
}

// 1. Собрать список компонентов из папок awds-component-*/component.meta.json
const components = readdirSync(ROOT, { withFileTypes: true })
  .filter(d => d.isDirectory() && d.name.startsWith('awds-component-'))
  .map(d => {
    const dir = d.name;
    const metaPath = join(ROOT, dir, 'component.meta.json');
    const previewPath = join(dir, 'references', 'preview.html');
    const hasPreview = existsSync(join(ROOT, previewPath));
    let component = dir.replace(/^awds-component-/, '');
    let variants = [];
    if (existsSync(metaPath)) {
      try {
        const meta = JSON.parse(readFileSync(metaPath, 'utf8'));
        if (meta.component) component = meta.component;
        if (meta.variants) variants = Object.keys(meta.variants);
      } catch { /* битый meta — берём имя из папки */ }
    }
    return { slug: component, dir, preview: previewPath, hasPreview, variants };
  })
  .filter(c => c.hasPreview);

// 2. Отсортировать по ORDER, остальное — алфавитом
components.sort((a, b) => {
  const ia = ORDER.indexOf(a.slug), ib = ORDER.indexOf(b.slug);
  if (ia !== -1 && ib !== -1) return ia - ib;
  if (ia !== -1) return -1;
  if (ib !== -1) return 1;
  return a.slug.localeCompare(b.slug);
});

// 3. HTML сайдбара
const navItems = components.map(c => {
  const sub = c.variants.length ? c.variants.join(' · ') : '';
  return `      <li>
        <a class="nav__link" href="#${c.slug}" data-slug="${c.slug}" data-src="${c.preview}">
          <span class="nav__title">${titleize(c.slug)}</span>
          ${sub ? `<span class="nav__sub">${sub}</span>` : ''}
        </a>
      </li>`;
}).join('\n');

const dataJson = JSON.stringify(
  components.map(c => ({ slug: c.slug, src: c.preview })),
);

const html = `<!DOCTYPE html>
<!-- AUTOGENERATED by tools/build-storybook.mjs. Перегенерировать: node tools/build-storybook.mjs -->
<html lang="ru" class="theme-default theme-light rounded-smooth">
<head>
  <meta charset="UTF-8">
  <title>ArrowDS · Components</title>
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <link rel="stylesheet" href="./arrow-design-system/references/css-variables.css">
  <link rel="stylesheet" href="./arrow-design-system/references/css-global.css">
  <style>
    * { box-sizing: border-box; }
    html, body { height: 100%; margin: 0; }
    body {
      display: grid; grid-template-columns: 280px 1fr; height: 100vh;
      font: 14px/1.5 var(--awds-font-family-system, system-ui), sans-serif;
      color: rgb(var(--surface-on-highest) / 1);
      background: rgb(var(--surface-surface) / 1);
    }

    /* ── Сайдбар ── */
    .side {
      display: flex; flex-direction: column; min-height: 0;
      background: rgb(var(--surface-container-lowest) / 1);
      border-right: 1px solid rgb(var(--surface-container-low) / 1);
    }
    .side__head {
      padding: var(--awds-space-5) var(--awds-space-5) var(--awds-space-4);
      border-bottom: 1px solid rgb(var(--surface-container-low) / 1);
    }
    .side__brand { font-size: 16px; font-weight: 700; margin: 0; letter-spacing: -0.01em; }
    .side__brand b { color: rgb(var(--primary-core) / 1); }
    .side__tagline { margin: 2px 0 0; font-size: 12px; color: rgb(var(--surface-on-low) / 1); }

    .side__theme {
      display: flex; align-items: center; gap: var(--awds-space-2);
      padding: var(--awds-space-3) var(--awds-space-5);
      border-bottom: 1px solid rgb(var(--surface-container-low) / 1);
      font-size: 12px; color: rgb(var(--surface-on-low) / 1);
    }
    .side__theme select {
      font: inherit; padding: var(--awds-space-1) var(--awds-space-2);
      border-radius: var(--awds-rounded-300);
      border: 1px solid rgb(var(--surface-container-high) / 1);
      background: rgb(var(--surface-container-lowest) / 1); color: inherit; cursor: pointer;
    }

    .nav { flex: 1; overflow-y: auto; padding: var(--awds-space-3) var(--awds-space-3) var(--awds-space-6); }
    .nav ul { list-style: none; margin: 0; padding: 0; }
    .nav__link {
      display: flex; flex-direction: column; gap: 1px; text-decoration: none;
      padding: var(--awds-space-2) var(--awds-space-3);
      border-radius: var(--awds-rounded-400); color: inherit;
      transition: background-color 0.12s ease-out, color 0.12s ease-out;
    }
    .nav__link:hover { background: rgb(var(--surface-container-low) / 1); }
    .nav__link.is-active { background: rgb(var(--primary-core) / 1); color: rgb(var(--primary-on) / 1); }
    .nav__link.is-active .nav__sub { color: rgb(var(--primary-on) / 0.75); }
    .nav__title { font-weight: 600; }
    .nav__sub {
      font-size: 11px; color: rgb(var(--surface-on-low) / 1);
      overflow: hidden; text-overflow: ellipsis; white-space: nowrap;
    }

    /* ── Контент ── */
    .stage { min-width: 0; min-height: 0; display: flex; }
    .stage iframe { flex: 1; width: 100%; height: 100%; border: 0; background: rgb(var(--surface-surface) / 1); }

    @media (max-width: 720px) {
      body { grid-template-columns: 1fr; grid-template-rows: auto 1fr; }
      .side { border-right: 0; border-bottom: 1px solid rgb(var(--surface-container-low) / 1); max-height: 45vh; }
    }
  </style>
</head>
<body>
  <aside class="side">
    <div class="side__head">
      <p class="side__brand"><b>Arrow</b>DS · Components</p>
      <p class="side__tagline">Storybook компонент-скиллов · <a href="https://github.com/OrazaevDesign/arrow-components" style="color:inherit;">GitHub</a></p>
    </div>
    <label class="side__theme">Тема
      <select id="theme">
        <option value="light">light</option>
        <option value="dark">dark</option>
      </select>
    </label>
    <nav class="nav">
      <ul>
${navItems}
      </ul>
    </nav>
  </aside>

  <main class="stage">
    <iframe id="frame" title="preview" loading="lazy"></iframe>
  </main>

  <script>
    const COMPONENTS = ${dataJson};
    const frame = document.getElementById('frame');
    const links = [...document.querySelectorAll('.nav__link')];
    const themeSel = document.getElementById('theme');

    function applyThemeToFrame() {
      try {
        const doc = frame.contentDocument;
        if (!doc) return;
        doc.documentElement.classList.remove('theme-light', 'theme-dark');
        doc.documentElement.classList.add('theme-' + themeSel.value);
        const inner = doc.getElementById('theme');
        if (inner) inner.value = themeSel.value;
      } catch (e) { /* кросс-ориджин — на Pages не случится */ }
    }

    function show(slug) {
      const item = COMPONENTS.find(c => c.slug === slug) || COMPONENTS[0];
      if (!item) return;
      links.forEach(a => a.classList.toggle('is-active', a.dataset.slug === item.slug));
      if (frame.dataset.slug !== item.slug) { frame.dataset.slug = item.slug; frame.src = item.src; }
    }

    function fromHash() { return decodeURIComponent((location.hash || '').replace(/^#/, '')); }

    frame.addEventListener('load', applyThemeToFrame);
    themeSel.addEventListener('change', () => {
      document.documentElement.classList.remove('theme-light', 'theme-dark');
      document.documentElement.classList.add('theme-' + themeSel.value);
      applyThemeToFrame();
    });
    window.addEventListener('hashchange', () => show(fromHash()));

    show(fromHash() || (COMPONENTS[0] && COMPONENTS[0].slug));
  </script>
</body>
</html>
`;

writeFileSync(join(ROOT, 'index.html'), html, 'utf8');
console.log(`index.html собран: ${components.length} компонент(ов) — ${components.map(c => c.slug).join(', ')}`);
